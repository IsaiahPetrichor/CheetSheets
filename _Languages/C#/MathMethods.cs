namespace c_sharp
{
    internal static class MathMethods
    {
        public static void GetMath()
        {
            // Math max returns greater of two
            Console.WriteLine(Math.Max(5, 6));

            // Math min returns lesser of two
            Console.WriteLine(Math.Min(5, 6));

            // Math sqrt returns the square root
            Console.WriteLine(Math.Sqrt(9));

            // Math abs returns the absolute
            Console.WriteLine(Math.Abs(-38572));

            // Math round returns the nearest whole number
            Console.WriteLine(Math.Round(2745.3497293));
        }
    }
}